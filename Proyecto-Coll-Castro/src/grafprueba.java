    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int count = 0;
        String selec = (String) jCBSelr.getSelectedItem();
        //JOptionPane.showMessageDialog(null, selec);
        String[] value_split = tableh.get(selec).split("\\|");
        JLTitout1.setText(value_split[0]);
        JLAutout1.setText(value_split[1]);
        JLPclaves1.setText(value_split[2]);
        jTextArea2.setText(value_split[3]);
        
        String palabrasclav = value_split[2];
        String[] palabrasclave;
        palabrasclave = palabrasclav.toLowerCase().split(",");
        //System.out.println(palabrasclave[0]);
        //System.out.println(palabrasclave[1]);
        //System.out.println(palabrasclave[2]);
        //System.out.println(palabrasclave[3]);
        String palabrastexto = value_split[3];
     
        String[] listWord;
        listWord = palabrastexto.replaceAll("[^a-zA-Z ]", "").toLowerCase().split("\\s+");
        int largo = listWord.length;

        // 1.Crear un Set, el Set no permite valores duplicados
        // se convierte listWord en una lista para usarlo de argumento
        // en el constructor del Set
        //Set<String> palabras = new HashSet<>(Arrays.asList(listWord));

        // 2. Se recorre el Set, no contiene palabras repetidas
        String totalPalabras = "";
        for(String word : palabrasclave){
            //System.out.println("Palabra a contar: "+word);
            word=word.trim();
            // la l√≥gica es la misma de antes
            int iguales = 0; count = 0;
            for (int j = 0; j < largo; j++) {
                String lword = listWord[j].trim();
                iguales = lword.compareTo(word);
                if (iguales ==0) {
                    //System.out.println("iguales a buscar : "+word+"Encontro "+lword);
                    count=count+1;
                }
                
            }
            if (count > 0) {
                System.out.println(word + " " + count);
                totalPalabras += "Palabra clave: "+word+" aparece "+count+" veces.\n";
            } else {
                System.out.println(word + " " + count);
                totalPalabras += "Palabra clave: "+word+" no aparece.\n";
            }

        }
        jTextArea2.setText(value_split[3]+"\n\n"+totalPalabras);
        
    }//GEN-LAST:event_jButton2ActionPerformed

